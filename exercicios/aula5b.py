nota = float(input('Digite a nota: '))

if nota >= 8.5:
    print('Sua nota está no conceito A.')

elif nota >= 7:
    print('Sua nota está no conceito B.')

elif nota >= 5:
    print('Sua nota está no conceito C.')

elif nota >= 3:
    print('Sua nota está no conceito D.')

else:
    print('Sua nota está no conceito E.')